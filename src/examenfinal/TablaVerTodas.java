    public void ccargarReporte (){
        n.setRowCount(0);
        for (int i= 0; i < reporte.size() ; i++){
            n.addRow(new Object[]{
            reporte.get(i).getNobreTarea(),
            reporte.get(i).getDescripcion(),
            reporte.get(i).getEncargado(),
            reporte.get(i).getEstado(),
            reporte.get(i).getFecha(),
             });
            
        }
    }
    
    

